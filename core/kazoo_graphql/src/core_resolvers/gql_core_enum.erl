-module(gql_core_enum).

-export([input/2
        ,output/2
        ]).

-include("kz_graphql.hrl").

%%------------------------------------------------------------------------------
%% @doc
%% @end
%%------------------------------------------------------------------------------
-spec input(any(), any()) -> any().
input(_K, Input) ->
    ?DEV_LOG("enum to lower case, ~s:~s", [_K, Input]),
    {'ok', kz_term:to_lower_binary(Input)}.

%%------------------------------------------------------------------------------
%% @doc
%% @end
%%------------------------------------------------------------------------------
-spec output(any(), any()) -> any().
output(_, Output) ->
    {'ok', kz_term:to_upper_binary(Output)}.
